
public class lampada {

	
	
	private boolean ligado; 

	public lampada() {
		ligado = true;	
		System.out.println("ligado");
	
	}
		public void ligar() {
			ligado = true;
		if (ligado == true) {
			System.out.println("Sua lampada está ligada");
		}
		}
		
		public void desligar() {
			ligado = false;
		if (ligado == false) {
			System.out.println("Sua lampada está desligada");
		}
		}

			
	
	
	

	

	
		

		
		}
	
	

