
public class lampada {

	
	
	private boolean ligado; 

	public lampada() {
		ligado = true;	 //lampada jã começa ligada
		System.out.println("ligado");
	
	}
		public void ligar() {
			ligado = true;
		if (ligado == true) {
			System.out.println("Sua lampada está ligada"); //lampada ligada, mostras mensagem na tela 
		}
		}
		
		public void desligar() {
			ligado = false; //lampada desligada
		if (ligado == false) {
			System.out.println("Sua lampada está desligada");//lampada desligada, mostras mensagem na tela
		}
		}

			
	
	
	

	

	
		

		
		}
	
	

